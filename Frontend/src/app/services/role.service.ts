const roleColors: Record<UserRole, string> = {
    coach: 'text-galio-orange',
    athlete: 'text-galio-lime',
    doctor: 'text-blue-400',
    nutritionist: 'text-emerald-400',
    manager: 'text-purple-400'
};

@Injectable({ providedIn: 'root' })
export class RoleService {
    private roleSubject = new BehaviorSubject<UserRole>('athlete');
    currentRole$ = this.roleSubject.asObservable();

    allRoles: UserRole[] = ['coach', 'athlete', 'doctor', 'nutritionist', 'manager'];

    constructor() {
        this.initializeUser();
    }

    public refreshRole() {
        this.initializeUser();
    }

    private initializeUser() {
        const userStr = localStorage.getItem('user');
        if (userStr) {
            try {
                const user = JSON.parse(userStr);
                // Priority: Use role from user object if it exists
                const role = (user.role as UserRole) || 'athlete';
                if (this.roleSubject.value !== role) {
                    this.setCurrentRole(role);
                }
            } catch (e) {
                console.error('Error parsing user from local storage', e);
            }
        }
    }

    get currentRole(): UserRole {
        return this.roleSubject.value;
    }

    get user(): UserProfile {
        const userStr = localStorage.getItem('user');
        let user: any = {};
        if (userStr) {
            user = JSON.parse(userStr);
        }

        return {
            name: `${user.first_name || ''} ${user.last_name || ''}`.trim() || user.username || 'User',
            role: this.currentRole,
            roleLabel: this.currentRole.charAt(0).toUpperCase() + this.currentRole.slice(1),
            avatar: user.profilePicture || ''
        };
    }

    get navItems(): NavItem[] {
        return navByRole[this.currentRole] || navByRole['athlete'];
    }

    get roleColor(): string {
        return roleColors[this.currentRole] || 'text-slate-400';
    }

    setCurrentRole(role: UserRole): void {
        this.roleSubject.next(role);
    }

    getRoleColor(role: UserRole): string {
        return roleColors[role];
    }
}
